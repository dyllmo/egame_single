-module(main).

-export([
	start/1,		%%-----启动
	stop/1			%%-----停止
]).

-include("game.hrl").

%%--------------------------------------------------------------------------------------------------------------
%% @doc
%% 启动
%% @end
%%--------------------------------------------------------------------------------------------------------------
start (Tid) ->
	common_start(),
	server_start(Tid),
	ok.
	 
%%--------------------------------------------------------------------------------------------------------------
%% @doc
%% 所有服务器启动
%% @end
%%--------------------------------------------------------------------------------------------------------------
common_start () ->
	ok.
	
%%--------------------------------------------------------------------------------------------------------------
%% @doc
%% 专属服务器启动
%% @end
%%--------------------------------------------------------------------------------------------------------------
server_start (_) -> 
	game:start_child({ranch,{ranch_sup,start_link,[]},supervisor}),
	wss_init:start(),
	https_init:start(),
	ok.

%%--------------------------------------------------------------------------------------------------------------
%% @doc
%% 停止
%% @end
%%--------------------------------------------------------------------------------------------------------------
%%-----网关服务器
stop (?SERVER_T_GATE) ->
	%%-----等待玩家退出
	online:wait_player_exit(),
	ok;
	
%%-----登陆服务器
stop (?SERVER_T_LOGIN) ->
	%%-----dump数据
	database_dump:do(),
	ok;
	
%%-----日志服务器
stop (?SERVER_T_LOGGER) ->
	ok;

%%-----游戏服务器
stop (?SERVER_T_LOGIC) ->
	%%-----dump数据
	database_dump:do(),
	ok;
	
stop (_) ->
	ok.